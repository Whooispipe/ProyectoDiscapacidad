

public class SeguimientoImpacto {
    private String efecto; // "Positivo" o "Negativo"

    public SeguimientoImpacto(String efecto) {
        this.efecto = efecto;
    }

    // Getters y Setters
    public String getEfecto() {
        return efecto;
    }

    public void setEfecto(String efecto) {
        this.efecto = efecto;
    }
}

